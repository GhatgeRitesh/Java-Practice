package Strivers_455.Step1_31;

public class O6_forLoops {
    // program for the study of for loops in java
    public static void main(String[] args)
    {
        for(int i=0; i<5;i++)
        {
            System.out.print(i);
        }
        System.out.println(" ");
        int[] arr ={1,2,3,4,5};
        for(int a:arr)// for each loop
        {
            System.out.print(a);
        }
    }
}
