package Strivers_455.Step1_31;

public class O7_whileLOOP {
    public static void main(String[] args) {
        // program to learn while loop
        int i=0;
        while(i<5)
        {
            System.out.print(i);
            i++;
        }
        System.out.println("");
        do{
            System.out.print(i);
            i--;
        }while(i>0);

    }
}
